#include "ca.h"

truth_t
ca_check_is_algebraic(const ca_t x, ca_ctx_t ctx)
{
    if (CA_IS_SPECIAL(x))
    {
        if (ca_is_unknown(x, ctx))
            return T_UNKNOWN;

        return T_FALSE;
    }

    if (x->field == CA_FIELD_ID_QQ ||
        ctx->fields[x->field].type == CA_FIELD_TYPE_NF)
        return T_TRUE;

    return T_UNKNOWN;
}

